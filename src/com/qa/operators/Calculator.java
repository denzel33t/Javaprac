package com.qa.operators;

public class Calculator {
	
	public int addition(int one, int two) {
		return one + two;
	}
	
	public int subtraction(int one, int two) {
		return one - two;
	}
	
	public double division(int one, int two) {
		return one / two;
	}
	
	public int multiply(int one, int two) {
		return one * two;
	}
	
}
